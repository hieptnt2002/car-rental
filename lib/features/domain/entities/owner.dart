class Owner {
  final int id;
  final String name;
  final String image;
  final String phone;
  final String email;
  final String descriptions;
  final String address;
  final double latitude;
  final double longitude;
  Owner({
    required this.id,
    required this.name,
    required this.image,
    required this.phone,
    required this.email,
    required this.descriptions,
    required this.address,
    required this.latitude,
    required this.longitude,
  });
}
